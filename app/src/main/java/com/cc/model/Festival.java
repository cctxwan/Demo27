package com.cc.model;

/**
 * 节日对象
 */
public class Festival {

    int id;

    String name;

    public Festival(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getId() {
        return id;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }
}
